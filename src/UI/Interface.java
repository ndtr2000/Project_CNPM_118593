    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        try{
            String name = jTextField1.getText();
            List <Person> people = null;
            if(name != null && name.trim().length() > 0 ) {
                people = personDAO.getPersonByName(name);
            }
            else{
                people = personDAO.getAllPeople();
            }
            PersonTableModel model = new PersonTableModel(people);
            jTable1.setModel(model);
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(jp2, "Error: "+ e, "Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
       
    }//GEN-LAST:event_jButton13ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            int row = jTable1.getSelectedRow();
            if(row <0){
                JOptionPane.showMessageDialog(jp2,"Please select a person","Warning",JOptionPane.ERROR_MESSAGE);
		return;
            }
            Person temp = (Person) jTable1.getValueAt(row, PersonTableModel.OBJECT_COL);
            EditForm editForm = new EditForm(jp2, temp, personDAO);
            editForm.setVisible(true);
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

